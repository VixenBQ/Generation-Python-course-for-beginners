str_num = input().split()
str_nums = []
for i in range(len(str_num)):
    str_nums.append(int(str_num[i]))
for i in range(len(str_num)):
    if i != len(str_num) - 1 :
        print(str_num[i] + '+', end='')
    else:
        print(str_num[i] + '=' + str(sum(str_nums)))

