def is_correct_bracket(text):
    count = 0
    for i in range(len(text)):
        if text[i] == '(':
            count += 1
        elif text[i] == ')':
            count -= 1
            if count < 0:
                return False
    return count == 0


text = input()
print(is_correct_bracket(text))
